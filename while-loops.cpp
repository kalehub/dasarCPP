
#include <iostream>
using namespace std;

int main(){
    // example of while-loops
    int a = 2;
    while(a <=6){
        cout << a << endl;
        a++;
    }


    // example of do-while-loops
    int b = 7;
    do {
        cout << b << endl;
        b++;} while( b<=6);






    return 0;
}





